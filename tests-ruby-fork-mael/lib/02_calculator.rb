def add (a,b)
	result = a + b
	return result
end	

def subtract (a,b)
	result = a - b
	return result
end

def sum (a)
return a.sum
end

def multiply (a,b)
	result = a * b
	return result
end

def power (a,b)
	result = a**b
	return result
end		

def factorial (a)
	result = Math.gamma(a+1)
	return result.to_i
end	